def log_trace(message: str) -> None:
    print('[TRACE] ' + message)


def log_info(message: str) -> None:
    print('[INFO] ' + message)


def log_warning(message: str) -> None:
    print('[WARNING] ' + message)


def log_error(message: str) -> None:
    print('[ERROR] ' + message)


def log_fatal(message: str) -> None:
    print('[FATAL] ' + message)
